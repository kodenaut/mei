     */
    public function update(Request $request, $id)
    {
        //
        $request->validate([
            'content'=>'required',
        ]);

        $vision = Vision::find($id);
        $vision->content = $request->get('content');

        $vision->save();
        return redirect('/admin')->with('success', 'Vision Has Been Updated!');
